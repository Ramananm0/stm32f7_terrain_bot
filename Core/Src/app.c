/* ── Timing ─────────────────────────────────────────────────────── */
#define LCD_PUB_PERIOD_MS    100   /*  10 Hz — LCD refresh            */
#define IMU_PERIOD_MS         10   /* 100 Hz — Madgwick + safety      */
#define ENC_PERIOD_MS         10   /* 100 Hz — encoder + motor PID    */
#define IMU_PUB_PERIOD_MS     10   /* 100 Hz — /imu/data publish      */
#define ENC_PUB_PERIOD_MS     20   /*  50 Hz — /wheel_ticks publish   */
#define CALIB_SAMPLES        200   /*   ~2 s at 100 Hz — gyro bias    */
#define MADGWICK_BETA       0.033f

/* ── Safety parameters — Mourougane & Dhanalakshmi paper ─────────
 *
 * Eq.5:  θ_w = W_PITCH × |pitch| + W_ROLL × |roll|   [degrees]
 * Eq.9:  R_incl = clamp((θ_w − THETA_SAFE) / (THETA_CRIT − THETA_SAFE), 0,1)
 * Eq.10: R_f = BETA × R_f_prev + (1−BETA) × R_raw
 * Eq.11: speed_scale = (1 − R_f)²
 * Eq.12: pitch > ESTOP_PITCH OR roll > ESTOP_ROLL → stop
 */
#define SAFETY_W_PITCH       0.7f    /* slope risk weight (pitch)          */
#define SAFETY_W_ROLL        0.3f    /* lateral instability weight (roll)  */
#define SAFETY_THETA_SAFE    10.0f   /* safe zone upper limit [deg]        */
#define SAFETY_THETA_CRIT    30.0f   /* E-stop boundary [deg]              */
#define SAFETY_W_INCL        0.7f    /* inclination fraction in R_raw      */
#define SAFETY_W_SHAKE       0.3f    /* vibration   fraction in R_raw      */
#define SAFETY_LPF_BETA      0.90f   /* low-pass β (paper §III-C)          */
#define SAFETY_ESTOP_PITCH   30.0f   /* hard E-stop pitch threshold [deg]  */
#define SAFETY_ESTOP_ROLL    40.0f   /* hard E-stop roll  threshold [deg]  */
/* Forward-flip detection (paper §VI future work) */
#define SAFETY_FLIP_RATE     (30.0f * 0.017453293f)  /* 30 deg/s → rad/s */
#define SAFETY_FLIP_PITCH    (15.0f * 0.017453293f)  /* 15 deg   → rad   */

/* ── cmd_vel watchdog ────────────────────────────────────────────── */
#define CMD_VEL_TIMEOUT_MS   500    /* stop motors if ROS silent > 500 ms */

/* ── Motor velocity PI controller ────────────────────────────────── */
#define MOTOR_KP     0.5f
#define MOTOR_KI     0.1f
#define MOTOR_ILIMIT (MOTOR_MAX_SPEED_MMPS * 0.5f)   /* anti-windup cap */

/* ── Noise covariance (ICM-20948 datasheet) ──────────────────────── */
/* Gyro  : 0.015 dps/√Hz @ 100 Hz → σ = 0.00262 rad/s               */
/* Accel : 230 µg/√Hz   @ 100 Hz → σ = 0.02256 m/s²                 */
#define GY_COV   (0.00262f * 0.00262f)
#define AC_COV   (0.02256f * 0.02256f)
#define OR_COV    0.0001f

#define UROS_OK(fn)  do { if ((fn) != RCL_RET_OK) Error_Handler(); } while(0)

