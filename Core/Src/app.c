/**
 * app.c  —  STM32F746G-DISCO  Main Application
 *
 * ── What this board does ────────────────────────────────────────────
 *   1. Reads ICM-20948 (9-DOF) at 102 Hz via I2C1
 *        I2C1: SCL = PB8 (Arduino SCL)   SDA = PB9 (Arduino SDA)
 *
 *   2. Runs Madgwick AHRS filter (quaternion orientation, 100 Hz)
 *        β = 0.033  (tuned for ICM-20948 @ 102 Hz)
 *
 *   3. Reads 4× RMCS-3070 encoders at 100 Hz
 *        FL : TIM5  A0(PA0=CH1)        + A1(PA1=CH2)
 *        FR : TIM2  D9(PA15=CH1)       + CN2p15(PB3=CH2)
 *        RL : TIM4  CN2p13(PB6=CH1)   + CN2p11(PB7=CH2)
 *        RR : TIM3  D3(PB4=CH1)        + CN2(PB5=CH2)
 *
 *   4. Drives 4× motors via TIM1 PWM (20 kHz) + direction GPIO
 *        FL : TIM1_CH1 → PA8(D10)     DIR → PG6(D2)
 *        FR : TIM1_CH2 → PE11(Morpho) DIR → PG7(D4)
 *        RL : TIM1_CH3 → PE13(Morpho) DIR → PI3(D7)
 *        RR : TIM1_CH4 → PE14(Morpho) DIR → PI2(D8)
 *
 *   5. Implements terrain-aware velocity safety layer (paper §III)
 *        IMU-based inclination risk  (Eq. 5,9)
 *        Terrain shake factor        (Eq. 6,7)
 *        Low-pass risk filter β=0.90 (Eq. 10)
 *        Nonlinear scaling (1−R)²    (Eq. 11)
 *        Hard E-stop at pitch>30°    (Eq. 12)
 *        Forward-flip detection (pitch_rate>30°/s + pitch>15°)
 *
 *   6. Receives /cmd_vel (geometry_msgs/Twist) from Nav2/SLAM on RPi
 *        Differential kinematics → per-wheel velocity targets
 *        Velocity PID (PI) closed-loop with encoder feedback
 *        Watchdog: stops motors if no /cmd_vel for 500 ms
 *
 *   7. Publishes via micro-ROS over USART6 (D1=TX, D0=RX) at 2 Mbaud:
 *        /imu/data          sensor_msgs/Imu              @ 100 Hz
 *        /wheel_ticks       std_msgs/Int32MultiArray      @  50 Hz
 *        /wheel_velocity    std_msgs/Float32MultiArray    @  50 Hz
 *
 * ── Reference ───────────────────────────────────────────────────────
 *   Safety layer:  Mourougane & Dhanalakshmi, "Terrain-Aware Velocity
 *   Regulation for Mobile Robots via Lightweight Execution-Level Safety
 *   Layer", SRM Institute of Science and Technology.
 *   Madgwick filter: Madgwick et al., IEEE ICORR 2011.
 *
 * ── CubeMX peripherals needed ───────────────────────────────────────
 *   I2C1  USART6  TIM5  TIM2  TIM4  TIM3  TIM1(PWM)
 *   GPIO: PG6 PG7 PI3 PI2 as outputs (motor direction)
 */

